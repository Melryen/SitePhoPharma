            <h1 style="color:#6E34C2; font-family: 'Allura', cursive; font-size: 28px; padding-left: 220px; ">O que é?</h1>
            <p style="padding-top: 20px; font-size: 20px; font-family: arial;">
             O autismo é considerado um transtorno mental de desenvolvimento que pode resultar  
             em problemas de comunicação ou interação social. É  importante destacar o caráter espectral
             do transtorno autista, o que significa que pessoas autistas podem apresentar diferentes níveis em seu
             quadro.
            </p>
            <center><img src="img/grau.jpeg" width="380px" height="160px" style="padding-left: 0px; padding-top: 20px;"/></center>
            <p style="padding-top: 25px; font-size: 20px; font-family: arial;">
            <h style='color:greenyellow'> Nível leve:</h> Também conhecido como alto funcionamento, apresentam sintomas bem sutis que não interferem
                muito no trabalho ou na escola, necessitando apenas de leves dicas;
            </p>
            <p style="padding-top: 10px; font-size: 20px; font-family: arial;">
            <h style='color:orange'> Nível moderado: </h>A partir deste grupos, a pessoa necessitará de um apoio maior a terapia da fala e intervenções
                que visem às habilidades sociais;
            </p>
            <p style="padding-top: 10px; font-size: 20px; font-family: arial;">
            <h style='color:red'> Nível grave:</h> As pessoas incluídas nesse nível mais severo, necessitam de ajuda não só no auxilio a fala
                ou a interação, mais também em pequenas tarefas cotidianos, como se alimentar ou vestir.
            </p>
        </div>
    </div>
        
