        <div class="title">
             <h1 style="color:#6E34C2; font-family: 'Allura', cursive; font-size: 28px; padding-left: 190px; " >Tratamentos</h1>
             <p style="padding-top: 20px; font-size: 20px; font-family: arial;">
             Quando falamos de tratamento para o autismo a primeira coisa que devemos saber é que não há uma cura
             e que antes de implementar qualquer tratamento na criança é preciso de eficácia científicamente comprovada. 
             Geralmente, o tratamento do autismo é composto por acompanhamento com psicólogos, fonoaudiólogos, terapeutas
             ocupacionais e até mesmo fisioterapeutas. Existem três principais técnicas desenvolvidas para o tratamento do TEA. 
            </p>
            <p style="padding-top: 20px; font-size: 20px; font-family: arial;">
             Um deles é o <h style='color:yellowgreen'>ABA</h>, conhecida aqui no Brasil Análise do Comportamento Aplicada. Que consiste em terapia comportamental,
             ou seja, trabalha com intervenções personalizadas para que o paciente saiba como reagir diante do mesmo cenário fora 
             do ambiente terapêutico. É ideal que seja aplicado de uma forma natural, se adaptando ao contexto de vida da criança.
             </p>
            <p style="padding-top: 20px; font-size: 20px; font-family: arial;">
             O <h style='color:orange'>TEACHH</h> (Tratamento e Educação para Autistas e Crianças com Déficits Relacionados à Comunicação) é outro dos 
             tratamentos para autismo de técnica comportamental. Tal modelo  busca avaliar a criança e identificar seus pontos fortes 
             e seus interesses, assim como as dificuldades. A partir daí, é possível desenvolver um programa individualizado.
             </p>
            <p style="padding-top: 20px; font-size: 20px; font-family: arial;">
             A técnica <h style='color:red'>PECS</h> (Sistema de Comunicação por Troca de Figuras), consiste em seis fases, tendo como objetivo promover a 
             comunicação funcional. O tratamento começa ensinando um indivíduo a trocar figuras com ações ou itens desejados a
             um “parceiro de comunicação” pra facilitar a comunicação entre ambos.O sistema prossegue ensinando a discriminação de 
             figuras e como juntá-las em frases, que poteriormente 
             Nas fases mais avançadas, os indivíduos são ensinados a usar iniciadores, responder perguntas e comentar.
             
            </p>
